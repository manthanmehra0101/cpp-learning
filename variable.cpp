    std::cout<< a<< std::endl;
    int b = 20;
    std::cout<< b<< std::endl;
    short c=30;
    std::cout<< c<< std::endl;
    short d= 'a';
    std::cout<< d<< std::endl;
    long e=40;
    std::cout<< e<< std::endl;
    long f='A';
